package Practice2.task_3;

public class Point {
    private double x;
    private double y;

    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    public void setX(double x){
       this.x = x;
    }
    public void setY(double y){
        this.y = y;
    }
    public void setXY(double x, double y){
        this.x = x;
        this.y = y;
    }
}
